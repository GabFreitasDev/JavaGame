    private void btnComecoJogoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnComecoJogoActionPerformed
        // TODO add your handling code here:
        if(txtNomeJ1.getText() == "" || txtNomeJ2.getText() == ""){
            JOptionPane.showMessageDialog(this,"Por favor, complete ambos os campos dos jogadores.");
        }
        else{
            jogador1.setNomeJogador(txtNomeJ1.getText());
            jogador2.setNomeJogador(txtNomeJ2.getText());
            
            TelaCombateGUI tela2 = new TelaCombateGUI(jogador1,jogador2);
            
            if(this.qualJogadorComeca() == jogador1){
                tela2.desabilitaMao(tela2.getBtn1CartaMao2(), tela2.getBtn2CartaMao2(), tela2.getBtn3CartaMao2(), tela2.getBtn4CartaMao2(), tela2.getBtn5CartaMao2(), tela2.getBtn1CampoDeBatalha2(), tela2.getBtn1CampoDeBatalha2(), tela2.getBtn1CampoDeBatalha2());
            }
            else{
                tela2.desabilitaMao(tela2.getBtn1CartaMao1(), tela2.getBtn2CartaMao1(), tela2.getBtn3CartaMao1(), tela2.getBtn4CartaMao1(), tela2.getBtn5CartaMao1(), tela2.getBtn1CampoDeBatalha1(), tela2.getBtn1CampoDeBatalha1(), tela2.getBtn1CampoDeBatalha1());
            }
            
            this.dispose();
            tela2.setVisible(true);
        }
    }//GEN-LAST:event_btnComecoJogoActionPerformed

