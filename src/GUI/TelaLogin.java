    public Jogadores qualJogadorComeca(){
        randomNum = random.nextInt(2);
        
        if(randomNum == 0){
            JOptionPane.showMessageDialog(this,this.jogador1.getNomeJogador()+" começa o jogo(de cima)!");
            return jogador1;
        }
        else{
            JOptionPane.showMessageDialog(this,this.jogador2.getNomeJogador()+" começa o jogo(de baixo)!");
            return jogador2;
        }
    }
    
